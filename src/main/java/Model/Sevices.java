public class Sevices {
    private int ID;
    private String SevicesName;
    private String SevicesDecription;
    private int Price;

    /**
     * @return the ID
     */
    public int getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(int ID) {
        this.ID = ID;
    }

    /**
     * @return the SevicesName
     */
    public String getSevicesName() {
        return SevicesName;
    }

    /**
     * @param SevicesName the SevicesName to set
     */
    public void setSevicesName(String SevicesName) {
        this.SevicesName = SevicesName;
    }

    /**
     * @return the SevicesDecription
     */
    public String getSevicesDecription() {
        return SevicesDecription;
    }

    /**
     * @param SevicesDecription the SevicesDecription to set
     */
    public void setSevicesDecription(String SevicesDecription) {
        this.SevicesDecription = SevicesDecription;
    }

    /**
     * @return the Price
     */
    public int getPrice() {
        return Price;
    }

    /**
     * @param Price the Price to set
     */
    public void setPrice(int Price) {
        this.Price = Price;
    }
    
}
